// Helper function to get a hackathon by ID
export const getHackathonById = (id) => {
  return hackathonsData.find(hackathon => hackathon.id === parseInt(id)) || null;
};

// Helper function to get featured hackathons (for homepage)
export const getFeaturedHackathons = () => {
  return hackathonsData;
};

// Get hackathons by location type
export const getHackathonsByLocationType = (locationType) => {
  if (locationType === 'all') return hackathonsData;
  return hackathonsData.filter(hackathon => hackathon.locationType === locationType);
};

// Get hackathons by category
export const getHackathonsByCategory = (category) => {
  if (category === 'all') return hackathonsData;
  return hackathonsData.filter(hackathon => hackathon.category === category);
};

// Search hackathons by query
export const searchHackathons = (query) => {
  if (!query) return hackathonsData;
  const lowercaseQuery = query.toLowerCase();
  return hackathonsData.filter(hackathon => 
    hackathon.title.toLowerCase().includes(lowercaseQuery) ||
    hackathon.description.toLowerCase().includes(lowercaseQuery)
  );
};

// Get all hackathons
export const getAllHackathons = () => {
  return hackathonsData;
};

export default hackathonsData; 